TRIGGER_PAYLOADS = [
    "?id=1'%20OR%20'1'='1",
    "?q=<script>alert(1)</script>",
    "?file=../../../etc/passwd",
    "?id=1%20UNION%20SELECT%201,2,3--",
    "?cmd=;ls%20-la",
]


def run(url, domain, timeout=10):
    section("WAF Detection", "🛡️")
    results = {"detected": [], "confidence": {}, "evidence": {}}

    resp = safe_request(url, timeout=timeout)
    if not resp:
        error("Could not connect to target.")
        return results

    headers_lower = {k.lower(): v.lower() for k, v in resp.headers.items()}
    cookies_lower = [c.lower() for c in resp.cookies.keys()]
    body_lower = resp.text.lower()

    # Probe with malicious payloads
    trigger_responses = []
    for payload in TRIGGER_PAYLOADS:
        r = safe_request(url + payload, timeout=timeout)
        if r:
            trigger_responses.append(r)

    scores = {}
    evidence_map = {}

    for waf_name, sigs in WAF_SIGNATURES.items():
        score = 0
        evidence = []

        # Header checks
        for hdr, val in sigs["headers"].items():
            if hdr in headers_lower:
                if val is None or val in headers_lower[hdr]:
                    score += 25
                    evidence.append(f"Header '{hdr}' found")

        # Cookie checks
        for ck in sigs["cookies"]:
            for cookie in cookies_lower:
                if ck.lower() in cookie:
                    score += 25
                    evidence.append(f"Cookie '{ck}' found")
                    break

        # Body checks on base response
        for pattern in sigs["body"]:
            if pattern.lower() in body_lower:
                score += 20
                evidence.append(f"Body pattern '{pattern}' found")

        # Trigger response checks
        for tr in trigger_responses:
            if tr.status_code in (403, 406, 429, 503):
                score += 10
                evidence.append(f"Trigger payload returned HTTP {tr.status_code}")
                break
            tr_body = tr.text.lower()
            for pattern in sigs["body"]:
                if pattern.lower() in tr_body:
                    score += 15
                    evidence.append(f"Trigger response body matched '{pattern}'")
                    break

        if score > 0:
            scores[waf_name] = min(score, 100)
            evidence_map[waf_name] = evidence

    if scores:
        top_waf = max(scores, key=scores.get)
        top_score = scores[top_waf]

        if top_score >= 60:
            confidence = "HIGH"
            conf_color = Colors.RED
        elif top_score >= 30:
            confidence = "MEDIUM"
            conf_color = Colors.YELLOW
        else:
            confidence = "LOW"
            conf_color = Colors.DIM

        info("WAF Detected", f"{Colors.BOLD}{top_waf}{Colors.RESET}")
        print(f"{Colors.GREEN}[+]{Colors.RESET} {Colors.BOLD}Confidence:{Colors.RESET} "
              f"{conf_color}{confidence} ({top_score}%){Colors.RESET}")
        print(f"{Colors.GREEN}[+]{Colors.RESET} {Colors.BOLD}Evidence:{Colors.RESET}")
        for ev in evidence_map[top_waf]:
            print(f"       {Colors.DIM}• {ev}{Colors.RESET}")

        if len(scores) > 1:
            others = [f"{n} ({s}%)" for n, s in sorted(scores.items(), key=lambda x: -x[1]) if n != top_waf]
            warn(f"Also matched: {', '.join(others)}")

        results["detected"] = list(scores.keys())
        results["primary"] = top_waf
        results["confidence"] = {n: f"{s}%" for n, s in scores.items()}
        results["evidence"] = evidence_map
    else:
        warn("No WAF detected or WAF not recognized.")
        results["detected"] = []

    return results
